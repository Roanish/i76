static void create_device(void)
{
    float prio = 1.0f;
    uint32_t qci_count = 1;
    VkDeviceQueueCreateInfo qci[2] = {
        {
            .sType            = VK_STRUCTURE_TYPE_DEVICE_QUEUE_CREATE_INFO,
            .queueFamilyIndex = s_gfx_family,
            .queueCount       = 1,
            .pQueuePriorities = &prio,
        }
    };
    if (s_present_family != s_gfx_family) {
        qci[1] = (VkDeviceQueueCreateInfo){
            .sType            = VK_STRUCTURE_TYPE_DEVICE_QUEUE_CREATE_INFO,
            .queueFamilyIndex = s_present_family,
            .queueCount       = 1,
            .pQueuePriorities = &prio,
        };
        qci_count = 2;
    }

    const char *exts[] = { VK_KHR_SWAPCHAIN_EXTENSION_NAME };

    VkDeviceCreateInfo ci = {
        .sType                   = VK_STRUCTURE_TYPE_DEVICE_CREATE_INFO,
        .queueCreateInfoCount    = qci_count,
        .pQueueCreateInfos       = qci,
        .enabledExtensionCount   = 1,
        .ppEnabledExtensionNames = exts,
    };
    VK_CHECK(vkCreateDevice(s_phys, &ci, NULL, &s_device));

    vkGetDeviceQueue(s_device, s_gfx_family,     0, &s_gfx_queue);
    vkGetDeviceQueue(s_device, s_present_family,  0, &s_present_queue);
}

/* -----------------------------------------------------------------------
 * Swapchain
 * ----------------------------------------------------------------------- */

static void create_swapchain(int win_w, int win_h)
{
    VkSurfaceCapabilitiesKHR caps;
    vkGetPhysicalDeviceSurfaceCapabilitiesKHR(s_phys, s_surface, &caps);

    /* Format — prefer BGRA8 sRGB */
    uint32_t fc;
    vkGetPhysicalDeviceSurfaceFormatsKHR(s_phys, s_surface, &fc, NULL);
    VkSurfaceFormatKHR *fmts = malloc(fc * sizeof(*fmts));
    vkGetPhysicalDeviceSurfaceFormatsKHR(s_phys, s_surface, &fc, fmts);

    /*
     * Prefer UNORM over SRGB. The original game fed raw palette byte values
     * directly to the display DAC with no gamma transform. SRGB would cause
     * the driver to apply gamma encoding to our already-display-ready palette
     * colours, making everything look wrong. UNORM writes them straight through.
     *
     * Both use VK_COLOR_SPACE_SRGB_NONLINEAR_KHR — that describes the monitor's
     * expected input encoding (sRGB/gamma), not a GPU-side transform.
     */
    VkSurfaceFormatKHR chosen = fmts[0];
    for (uint32_t i = 0; i < fc; i++) {
        if (fmts[i].colorSpace != VK_COLOR_SPACE_SRGB_NONLINEAR_KHR) continue;
        if (fmts[i].format == VK_FORMAT_B8G8R8A8_UNORM) { chosen = fmts[i]; break; }
        if (fmts[i].format == VK_FORMAT_R8G8B8A8_UNORM && chosen.format != VK_FORMAT_B8G8R8A8_UNORM)
            chosen = fmts[i];
    }
    free(fmts);
    s_sc_format = chosen.format;
    fprintf(stdout, "[render_vk] Swapchain format: %s\n",
            chosen.format == VK_FORMAT_B8G8R8A8_UNORM ? "B8G8R8A8_UNORM" :
            chosen.format == VK_FORMAT_R8G8B8A8_UNORM ? "R8G8B8A8_UNORM" :
            chosen.format == VK_FORMAT_B8G8R8A8_SRGB  ? "B8G8R8A8_SRGB (fallback — colours may be off)" :
            "other");

    /* Present mode — prefer mailbox (no tearing, low latency) */
    uint32_t pmc;
    vkGetPhysicalDeviceSurfacePresentModesKHR(s_phys, s_surface, &pmc, NULL);
    VkPresentModeKHR *pms = malloc(pmc * sizeof(*pms));
    vkGetPhysicalDeviceSurfacePresentModesKHR(s_phys, s_surface, &pmc, pms);

    VkPresentModeKHR pm = VK_PRESENT_MODE_FIFO_KHR;
    for (uint32_t i = 0; i < pmc; i++)
        if (pms[i] == VK_PRESENT_MODE_MAILBOX_KHR) { pm = pms[i]; break; }
    free(pms);

    /* Extent */
    if (caps.currentExtent.width != UINT32_MAX) {
        s_sc_extent = caps.currentExtent;
    } else {
        s_sc_extent = (VkExtent2D){
            .width  = (uint32_t)win_w,
            .height = (uint32_t)win_h,
        };
    }

    uint32_t img_count = caps.minImageCount + 1;
    if (caps.maxImageCount && img_count > caps.maxImageCount)
        img_count = caps.maxImageCount;

    VkSwapchainCreateInfoKHR sci = {
        .sType            = VK_STRUCTURE_TYPE_SWAPCHAIN_CREATE_INFO_KHR,
        .surface          = s_surface,
        .minImageCount    = img_count,
        .imageFormat      = chosen.format,
        .imageColorSpace  = chosen.colorSpace,
        .imageExtent      = s_sc_extent,
        .imageArrayLayers = 1,
        .imageUsage       = VK_IMAGE_USAGE_COLOR_ATTACHMENT_BIT,
        .preTransform     = caps.currentTransform,
        .compositeAlpha   = VK_COMPOSITE_ALPHA_OPAQUE_BIT_KHR,
        .presentMode      = pm,
        .clipped          = VK_TRUE,
    };

    uint32_t qfi[2] = { s_gfx_family, s_present_family };
    if (s_gfx_family != s_present_family) {
        sci.imageSharingMode      = VK_SHARING_MODE_CONCURRENT;
        sci.queueFamilyIndexCount = 2;
        sci.pQueueFamilyIndices   = qfi;
    } else {
        sci.imageSharingMode = VK_SHARING_MODE_EXCLUSIVE;
    }

    VK_CHECK(vkCreateSwapchainKHR(s_device, &sci, NULL, &s_swapchain));

    vkGetSwapchainImagesKHR(s_device, s_swapchain, &s_sc_count, NULL);
    s_sc_images = malloc(s_sc_count * sizeof(*s_sc_images));
    vkGetSwapchainImagesKHR(s_device, s_swapchain, &s_sc_count, s_sc_images);

    s_sc_views = malloc(s_sc_count * sizeof(*s_sc_views));
    for (uint32_t i = 0; i < s_sc_count; i++) {
        VkImageViewCreateInfo vi = {
            .sType      = VK_STRUCTURE_TYPE_IMAGE_VIEW_CREATE_INFO,
            .image      = s_sc_images[i],
            .viewType   = VK_IMAGE_VIEW_TYPE_2D,
            .format     = s_sc_format,
            .components = {
                VK_COMPONENT_SWIZZLE_IDENTITY,
                VK_COMPONENT_SWIZZLE_IDENTITY,
                VK_COMPONENT_SWIZZLE_IDENTITY,
                VK_COMPONENT_SWIZZLE_IDENTITY,
            },
            .subresourceRange = { VK_IMAGE_ASPECT_COLOR_BIT, 0, 1, 0, 1 },
        };
        VK_CHECK(vkCreateImageView(s_device, &vi, NULL, &s_sc_views[i]));
    }
}

/* -----------------------------------------------------------------------
 * Render pass
 * ----------------------------------------------------------------------- */

static void create_render_pass(void)
{
    VkAttachmentDescription att = {
        .format         = s_sc_format,
        .samples        = VK_SAMPLE_COUNT_1_BIT,
        .loadOp         = VK_ATTACHMENT_LOAD_OP_CLEAR,
        .storeOp        = VK_ATTACHMENT_STORE_OP_STORE,
        .stencilLoadOp  = VK_ATTACHMENT_LOAD_OP_DONT_CARE,
        .stencilStoreOp = VK_ATTACHMENT_STORE_OP_DONT_CARE,
        .initialLayout  = VK_IMAGE_LAYOUT_UNDEFINED,
        .finalLayout    = VK_IMAGE_LAYOUT_PRESENT_SRC_KHR,
    };
    VkAttachmentReference ref = {
        .attachment = 0,
        .layout     = VK_IMAGE_LAYOUT_COLOR_ATTACHMENT_OPTIMAL,
    };
    VkSubpassDescription sp = {
        .pipelineBindPoint    = VK_PIPELINE_BIND_POINT_GRAPHICS,
        .colorAttachmentCount = 1,
        .pColorAttachments    = &ref,
    };
    VkSubpassDependency dep = {
        .srcSubpass    = VK_SUBPASS_EXTERNAL,
        .dstSubpass    = 0,
        .srcStageMask  = VK_PIPELINE_STAGE_COLOR_ATTACHMENT_OUTPUT_BIT,
        .srcAccessMask = 0,
        .dstStageMask  = VK_PIPELINE_STAGE_COLOR_ATTACHMENT_OUTPUT_BIT,
        .dstAccessMask = VK_ACCESS_COLOR_ATTACHMENT_WRITE_BIT,
    };
    VkRenderPassCreateInfo rpi = {
        .sType           = VK_STRUCTURE_TYPE_RENDER_PASS_CREATE_INFO,
        .attachmentCount = 1, .pAttachments  = &att,
        .subpassCount    = 1, .pSubpasses    = &sp,
        .dependencyCount = 1, .pDependencies = &dep,
    };
    VK_CHECK(vkCreateRenderPass(s_device, &rpi, NULL, &s_render_pass));
}

/* -----------------------------------------------------------------------
 * Descriptor set layout
 * ----------------------------------------------------------------------- */

static void create_descriptor_set_layout(void)
{
    VkDescriptorSetLayoutBinding bindings[2] = {
        {
            .binding         = 0,
            .descriptorType  = VK_DESCRIPTOR_TYPE_COMBINED_IMAGE_SAMPLER,
            .descriptorCount = 1,
            .stageFlags      = VK_SHADER_STAGE_FRAGMENT_BIT,
        },
        {
            .binding         = 1,
            .descriptorType  = VK_DESCRIPTOR_TYPE_UNIFORM_BUFFER,
            .descriptorCount = 1,
            .stageFlags      = VK_SHADER_STAGE_FRAGMENT_BIT,
        },
    };
    VkDescriptorSetLayoutCreateInfo ci = {
        .sType        = VK_STRUCTURE_TYPE_DESCRIPTOR_SET_LAYOUT_CREATE_INFO,
        .bindingCount = 2,
        .pBindings    = bindings,
    };
    VK_CHECK(vkCreateDescriptorSetLayout(s_device, &ci, NULL, &s_dsl));
}

/* -----------------------------------------------------------------------
 * Pipeline
 * ----------------------------------------------------------------------- */

static VkShaderModule make_shader(const uint32_t *code, uint32_t len)
{
    VkShaderModuleCreateInfo ci = {
        .sType    = VK_STRUCTURE_TYPE_SHADER_MODULE_CREATE_INFO,
        .codeSize = len,
        .pCode    = code,
    };
    VkShaderModule m;
    VK_CHECK(vkCreateShaderModule(s_device, &ci, NULL, &m));
    return m;
}

static void create_pipeline(void)
{
    VkShaderModule vert = make_shader(blit_vert_spv, blit_vert_spv_len);
    VkShaderModule frag = make_shader(blit_frag_spv, blit_frag_spv_len);

    VkPipelineShaderStageCreateInfo stages[2] = {
        {
            .sType  = VK_STRUCTURE_TYPE_PIPELINE_SHADER_STAGE_CREATE_INFO,
            .stage  = VK_SHADER_STAGE_VERTEX_BIT,
            .module = vert,
            .pName  = "main",
        },
        {
            .sType  = VK_STRUCTURE_TYPE_PIPELINE_SHADER_STAGE_CREATE_INFO,
            .stage  = VK_SHADER_STAGE_FRAGMENT_BIT,
            .module = frag,
            .pName  = "main",
        },
    };

    VkPipelineVertexInputStateCreateInfo vi = {
        .sType = VK_STRUCTURE_TYPE_PIPELINE_VERTEX_INPUT_STATE_CREATE_INFO,
    };
    VkPipelineInputAssemblyStateCreateInfo ia = {
        .sType    = VK_STRUCTURE_TYPE_PIPELINE_INPUT_ASSEMBLY_STATE_CREATE_INFO,
        .topology = VK_PRIMITIVE_TOPOLOGY_TRIANGLE_LIST,
    };

    VkViewport viewport = {
        .x        = 0.0f, .y        = 0.0f,
        .width    = (float)s_sc_extent.width,
        .height   = (float)s_sc_extent.height,
        .minDepth = 0.0f, .maxDepth = 1.0f,
    };
    VkRect2D scissor = { .offset = {0,0}, .extent = s_sc_extent };
    VkPipelineViewportStateCreateInfo vp = {
        .sType         = VK_STRUCTURE_TYPE_PIPELINE_VIEWPORT_STATE_CREATE_INFO,
        .viewportCount = 1, .pViewports = &viewport,
        .scissorCount  = 1, .pScissors  = &scissor,
    };

    VkPipelineRasterizationStateCreateInfo rs = {
        .sType       = VK_STRUCTURE_TYPE_PIPELINE_RASTERIZATION_STATE_CREATE_INFO,
        .polygonMode = VK_POLYGON_MODE_FILL,
        .cullMode    = VK_CULL_MODE_NONE,
        .frontFace   = VK_FRONT_FACE_CLOCKWISE,
        .lineWidth   = 1.0f,
    };
    VkPipelineMultisampleStateCreateInfo ms = {
        .sType                = VK_STRUCTURE_TYPE_PIPELINE_MULTISAMPLE_STATE_CREATE_INFO,
        .rasterizationSamples = VK_SAMPLE_COUNT_1_BIT,
    };

    VkPipelineColorBlendAttachmentState cba = {
        .colorWriteMask = VK_COLOR_COMPONENT_R_BIT | VK_COLOR_COMPONENT_G_BIT |
                          VK_COLOR_COMPONENT_B_BIT | VK_COLOR_COMPONENT_A_BIT,
    };
    VkPipelineColorBlendStateCreateInfo cb = {
        .sType           = VK_STRUCTURE_TYPE_PIPELINE_COLOR_BLEND_STATE_CREATE_INFO,
        .attachmentCount = 1,
        .pAttachments    = &cba,
    };

    VkPipelineLayoutCreateInfo pli = {
        .sType          = VK_STRUCTURE_TYPE_PIPELINE_LAYOUT_CREATE_INFO,
        .setLayoutCount = 1,
        .pSetLayouts    = &s_dsl,
    };
    VK_CHECK(vkCreatePipelineLayout(s_device, &pli, NULL, &s_pipeline_layout));

    VkGraphicsPipelineCreateInfo gpi = {
        .sType               = VK_STRUCTURE_TYPE_GRAPHICS_PIPELINE_CREATE_INFO,
        .stageCount          = 2,
        .pStages             = stages,
        .pVertexInputState   = &vi,
        .pInputAssemblyState = &ia,
        .pViewportState      = &vp,
        .pRasterizationState = &rs,
        .pMultisampleState   = &ms,
        .pColorBlendState    = &cb,
        .layout              = s_pipeline_layout,
        .renderPass          = s_render_pass,
        .subpass             = 0,
    };
    VK_CHECK(vkCreateGraphicsPipelines(s_device, VK_NULL_HANDLE, 1, &gpi,
                                       NULL, &s_pipeline));

    vkDestroyShaderModule(s_device, vert, NULL);
    vkDestroyShaderModule(s_device, frag, NULL);
}

/* -----------------------------------------------------------------------
 * Framebuffers
 * ----------------------------------------------------------------------- */

static void create_framebuffers(void)
{
    s_sc_fbs = malloc(s_sc_count * sizeof(*s_sc_fbs));
    for (uint32_t i = 0; i < s_sc_count; i++) {
        VkFramebufferCreateInfo fi = {
            .sType           = VK_STRUCTURE_TYPE_FRAMEBUFFER_CREATE_INFO,
            .renderPass      = s_render_pass,
            .attachmentCount = 1,
            .pAttachments    = &s_sc_views[i],
            .width           = s_sc_extent.width,
            .height          = s_sc_extent.height,
            .layers          = 1,
        };
        VK_CHECK(vkCreateFramebuffer(s_device, &fi, NULL, &s_sc_fbs[i]));
    }
}

/* -----------------------------------------------------------------------
 * Command pool + buffers
 * ----------------------------------------------------------------------- */

static void create_command_pool(void)
{
    VkCommandPoolCreateInfo ci = {
        .sType            = VK_STRUCTURE_TYPE_COMMAND_POOL_CREATE_INFO,
        .queueFamilyIndex = s_gfx_family,
        .flags            = VK_COMMAND_POOL_CREATE_RESET_COMMAND_BUFFER_BIT,
    };
    VK_CHECK(vkCreateCommandPool(s_device, &ci, NULL, &s_cmd_pool));

    VkCommandBufferAllocateInfo ai = {
        .sType              = VK_STRUCTURE_TYPE_COMMAND_BUFFER_ALLOCATE_INFO,
        .commandPool        = s_cmd_pool,
        .level              = VK_COMMAND_BUFFER_LEVEL_PRIMARY,
        .commandBufferCount = MAX_FRAMES_IN_FLIGHT,
    };
    VK_CHECK(vkAllocateCommandBuffers(s_device, &ai, s_cmd_bufs));
}

/* -----------------------------------------------------------------------
 * Sync objects
 * ----------------------------------------------------------------------- */

static void create_sync(void)
{
    VkSemaphoreCreateInfo si = { .sType = VK_STRUCTURE_TYPE_SEMAPHORE_CREATE_INFO };
    VkFenceCreateInfo fi = {
        .sType = VK_STRUCTURE_TYPE_FENCE_CREATE_INFO,
        .flags = VK_FENCE_CREATE_SIGNALED_BIT,
    };
    for (int i = 0; i < MAX_FRAMES_IN_FLIGHT; i++) {
        VK_CHECK(vkCreateSemaphore(s_device, &si, NULL, &s_sem_image_ready[i]));
        VK_CHECK(vkCreateFence(s_device, &fi, NULL, &s_fence[i]));
    }
    /* Render-done semaphores: one per swapchain image, indexed by s_image_idx.
     * Indexing by frame-in-flight is wrong — the presentation engine holds the
     * semaphore until the image is re-acquired, which may be > MAX_FRAMES later. */
    s_sem_render_done = malloc(s_sc_count * sizeof(VkSemaphore));
    for (uint32_t i = 0; i < s_sc_count; i++)
        VK_CHECK(vkCreateSemaphore(s_device, &si, NULL, &s_sem_render_done[i]));
}

/* -----------------------------------------------------------------------
 * Palette UBO
 * ----------------------------------------------------------------------- */

static void create_palette_buffer(void)
{
    make_buffer(PALETTE_UBO_SIZE,
                VK_BUFFER_USAGE_UNIFORM_BUFFER_BIT,
                VK_MEMORY_PROPERTY_HOST_VISIBLE_BIT |
                VK_MEMORY_PROPERTY_HOST_COHERENT_BIT,
                &s_pal_buf, &s_pal_mem);
    vkMapMemory(s_device, s_pal_mem, 0, PALETTE_UBO_SIZE, 0, &s_pal_map);

    /* Default palette: all black */
    memset(s_pal_map, 0, PALETTE_UBO_SIZE);
}

/* -----------------------------------------------------------------------
 * Index texture
 * ----------------------------------------------------------------------- */

static void create_index_texture(uint32_t w, uint32_t h)
{
    if (s_idx_image) {
        /* Destroy old resources before resize */
        vkDestroyImageView(s_device, s_idx_view, NULL);
        vkDestroyImage(s_device, s_idx_image, NULL);
        vkFreeMemory(s_device, s_idx_memory, NULL);
        vkUnmapMemory(s_device, s_idx_staging_mem);
        vkDestroyBuffer(s_device, s_idx_staging, NULL);
        vkFreeMemory(s_device, s_idx_staging_mem, NULL);
    }

    s_idx_w = w;
    s_idx_h = h;
    VkDeviceSize size = (VkDeviceSize)w * h;

    /* Device-local image */
    make_image(w, h, VK_FORMAT_R8_UNORM,
               VK_IMAGE_USAGE_TRANSFER_DST_BIT | VK_IMAGE_USAGE_SAMPLED_BIT,
               VK_MEMORY_PROPERTY_DEVICE_LOCAL_BIT,
               &s_idx_image, &s_idx_memory);

    /* Transition to shader-read layout on first use */
    VkCommandBuffer cmd = begin_one_time();
    transition_image_layout(cmd, s_idx_image,
                            VK_IMAGE_LAYOUT_UNDEFINED,
                            VK_IMAGE_LAYOUT_TRANSFER_DST_OPTIMAL);
    end_one_time(cmd);

    /* Staging buffer — persistently mapped */
    make_buffer(size,
                VK_BUFFER_USAGE_TRANSFER_SRC_BIT,
                VK_MEMORY_PROPERTY_HOST_VISIBLE_BIT |
                VK_MEMORY_PROPERTY_HOST_COHERENT_BIT,
                &s_idx_staging, &s_idx_staging_mem);
    vkMapMemory(s_device, s_idx_staging_mem, 0, size, 0, &s_idx_staging_map);

    /* Image view */
    VkImageViewCreateInfo vi = {
        .sType      = VK_STRUCTURE_TYPE_IMAGE_VIEW_CREATE_INFO,
        .image      = s_idx_image,
        .viewType   = VK_IMAGE_VIEW_TYPE_2D,
        .format     = VK_FORMAT_R8_UNORM,
        .components = {
            VK_COMPONENT_SWIZZLE_IDENTITY,
            VK_COMPONENT_SWIZZLE_IDENTITY,
            VK_COMPONENT_SWIZZLE_IDENTITY,
            VK_COMPONENT_SWIZZLE_IDENTITY,
        },
        .subresourceRange = { VK_IMAGE_ASPECT_COLOR_BIT, 0, 1, 0, 1 },
    };
    VK_CHECK(vkCreateImageView(s_device, &vi, NULL, &s_idx_view));

    s_idx_first_upload = true;
}

