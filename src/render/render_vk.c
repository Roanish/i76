bool render_init(void)
{
    SDL_Window *win = platform_get_window();
    if (!win) { fprintf(stderr, "[render_vk] No window\n"); return false; }

    int win_w, win_h;
    SDL_Vulkan_GetDrawableSize(win, &win_w, &win_h);

    create_instance(win);

    if (!SDL_Vulkan_CreateSurface(win, s_instance, &s_surface)) {
        fprintf(stderr, "[render_vk] SDL_Vulkan_CreateSurface: %s\n", SDL_GetError());
        return false;
    }

    pick_device();
    create_device();
    create_swapchain(win_w, win_h);
    create_render_pass();
    create_descriptor_set_layout();
    create_sampler();
    create_palette_buffer();
    create_command_pool();
    /* Index texture created on first render_blit_indexed call */
    create_descriptor_set();
    create_pipeline();
    create_framebuffers();
    create_sync();

    fprintf(stdout, "[render_vk] Vulkan ready (%ux%u, %u swapchain images)\n",
            s_sc_extent.width, s_sc_extent.height, s_sc_count);
    return true;
}

void render_shutdown(void)
{
    vkDeviceWaitIdle(s_device);

    for (int i = 0; i < MAX_FRAMES_IN_FLIGHT; i++) {
        vkDestroySemaphore(s_device, s_sem_image_ready[i], NULL);
        vkDestroyFence(s_device, s_fence[i], NULL);
    }
    for (uint32_t i = 0; i < s_sc_count; i++)
        vkDestroySemaphore(s_device, s_sem_render_done[i], NULL);
    free(s_sem_render_done);

    for (uint32_t i = 0; i < s_sc_count; i++) {
        vkDestroyFramebuffer(s_device, s_sc_fbs[i], NULL);
        vkDestroyImageView(s_device, s_sc_views[i], NULL);
    }
    free(s_sc_fbs); free(s_sc_views); free(s_sc_images);

    if (s_idx_image) {
        vkDestroyImageView(s_device, s_idx_view, NULL);
        vkDestroyImage(s_device, s_idx_image, NULL);
        vkFreeMemory(s_device, s_idx_memory, NULL);
        vkUnmapMemory(s_device, s_idx_staging_mem);
        vkDestroyBuffer(s_device, s_idx_staging, NULL);
        vkFreeMemory(s_device, s_idx_staging_mem, NULL);
    }

    vkUnmapMemory(s_device, s_pal_mem);
    vkDestroyBuffer(s_device, s_pal_buf, NULL);
    vkFreeMemory(s_device, s_pal_mem, NULL);

    vkDestroyDescriptorPool(s_device, s_desc_pool, NULL);
    vkDestroyDescriptorSetLayout(s_device, s_dsl, NULL);
    vkDestroySampler(s_device, s_sampler, NULL);
    vkDestroyPipeline(s_device, s_pipeline, NULL);
    vkDestroyPipelineLayout(s_device, s_pipeline_layout, NULL);
    vkDestroyRenderPass(s_device, s_render_pass, NULL);
    vkDestroyCommandPool(s_device, s_cmd_pool, NULL);
    vkDestroySwapchainKHR(s_device, s_swapchain, NULL);
    vkDestroyDevice(s_device, NULL);
    vkDestroySurfaceKHR(s_instance, s_surface, NULL);
    vkDestroyInstance(s_instance, NULL);
}

void render_begin_frame(void)
{
    vkWaitForFences(s_device, 1, &s_fence[s_frame], VK_TRUE, UINT64_MAX);

    VkResult r = vkAcquireNextImageKHR(s_device, s_swapchain, UINT64_MAX,
                                       s_sem_image_ready[s_frame],
                                       VK_NULL_HANDLE, &s_image_idx);
    if (r == VK_ERROR_OUT_OF_DATE_KHR || r == VK_SUBOPTIMAL_KHR) {
        /* TODO: recreate swapchain on resize */
        fprintf(stderr, "[render_vk] Swapchain out of date — resize not yet handled\n");
    }

    vkResetFences(s_device, 1, &s_fence[s_frame]);
    vkResetCommandBuffer(s_cmd_bufs[s_frame], 0);
}

void render_end_frame(void)
{
    VkCommandBuffer cmd = s_cmd_bufs[s_frame];

    VkCommandBufferBeginInfo bi = {
        .sType = VK_STRUCTURE_TYPE_COMMAND_BUFFER_BEGIN_INFO,
    };
    vkBeginCommandBuffer(cmd, &bi);

    /* Upload index texture if dirty */
    if (s_pending_blit && s_idx_image) {
        VkImageLayout from = s_idx_first_upload
            ? VK_IMAGE_LAYOUT_TRANSFER_DST_OPTIMAL
            : VK_IMAGE_LAYOUT_SHADER_READ_ONLY_OPTIMAL;

        if (!s_idx_first_upload)
            transition_image_layout(cmd, s_idx_image, from,
                                    VK_IMAGE_LAYOUT_TRANSFER_DST_OPTIMAL);

        VkBufferImageCopy region = {
            .imageSubresource = { VK_IMAGE_ASPECT_COLOR_BIT, 0, 0, 1 },
            .imageExtent      = { s_idx_w, s_idx_h, 1 },
        };
        vkCmdCopyBufferToImage(cmd, s_idx_staging, s_idx_image,
                               VK_IMAGE_LAYOUT_TRANSFER_DST_OPTIMAL, 1, &region);

        transition_image_layout(cmd, s_idx_image,
                                VK_IMAGE_LAYOUT_TRANSFER_DST_OPTIMAL,
                                VK_IMAGE_LAYOUT_SHADER_READ_ONLY_OPTIMAL);

        if (s_idx_first_upload) {
            /* First upload: bind the image into the descriptor set now */
            update_descriptor_set();
            s_idx_first_upload = false;
        }

        s_pending_blit = false;
    }

    /* Render pass */
    VkClearValue clear = { .color = {{0,0,0,1}} };
    VkRenderPassBeginInfo rpi = {
        .sType           = VK_STRUCTURE_TYPE_RENDER_PASS_BEGIN_INFO,
        .renderPass      = s_render_pass,
        .framebuffer     = s_sc_fbs[s_image_idx],
        .renderArea      = { .extent = s_sc_extent },
        .clearValueCount = 1,
        .pClearValues    = &clear,
    };
    vkCmdBeginRenderPass(cmd, &rpi, VK_SUBPASS_CONTENTS_INLINE);

    if (!s_idx_first_upload) {
        vkCmdBindPipeline(cmd, VK_PIPELINE_BIND_POINT_GRAPHICS, s_pipeline);
        vkCmdBindDescriptorSets(cmd, VK_PIPELINE_BIND_POINT_GRAPHICS,
                                s_pipeline_layout, 0, 1, &s_desc_set, 0, NULL);
        vkCmdDraw(cmd, 3, 1, 0, 0);
    }

    vkCmdEndRenderPass(cmd);
    vkEndCommandBuffer(cmd);

    VkPipelineStageFlags wait_stage = VK_PIPELINE_STAGE_COLOR_ATTACHMENT_OUTPUT_BIT;
    VkSubmitInfo si = {
        .sType                = VK_STRUCTURE_TYPE_SUBMIT_INFO,
        .waitSemaphoreCount   = 1,
        .pWaitSemaphores      = &s_sem_image_ready[s_frame],
        .pWaitDstStageMask    = &wait_stage,
        .commandBufferCount   = 1,
        .pCommandBuffers      = &cmd,
        .signalSemaphoreCount = 1,
        .pSignalSemaphores    = &s_sem_render_done[s_image_idx],
    };
    VK_CHECK(vkQueueSubmit(s_gfx_queue, 1, &si, s_fence[s_frame]));

    VkPresentInfoKHR pi = {
        .sType              = VK_STRUCTURE_TYPE_PRESENT_INFO_KHR,
        .waitSemaphoreCount = 1,
        .pWaitSemaphores    = &s_sem_render_done[s_image_idx],
        .swapchainCount     = 1,
        .pSwapchains        = &s_swapchain,
        .pImageIndices      = &s_image_idx,
    };
    vkQueuePresentKHR(s_present_queue, &pi);

    s_frame = (s_frame + 1) % MAX_FRAMES_IN_FLIGHT;
    s_pending_palette = false;
}

void render_set_palette(const Rgb8 *palette_256)
{
    /* Palette UBO layout (std140): 256 × vec4, each vec4 = {r,g,b,1} floats */
    float *dst = (float *)s_pal_map;
    for (int i = 0; i < 256; i++) {
        dst[i * 4 + 0] = palette_256[i].r / 255.0f;
        dst[i * 4 + 1] = palette_256[i].g / 255.0f;
        dst[i * 4 + 2] = palette_256[i].b / 255.0f;
        dst[i * 4 + 3] = 1.0f;
    }
    s_pending_palette = true;
}

void render_blit_indexed(const uint8_t *pixels, uint32_t w, uint32_t h)
{
    if (w != s_idx_w || h != s_idx_h)
        create_index_texture(w, h);

    memcpy(s_idx_staging_map, pixels, (size_t)w * h);
    s_pending_blit = true;
}
