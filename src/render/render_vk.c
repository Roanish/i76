static bool layer_available(const char *name)
{
    uint32_t count = 0;
    vkEnumerateInstanceLayerProperties(&count, NULL);
    VkLayerProperties *props = malloc(count * sizeof(*props));
    vkEnumerateInstanceLayerProperties(&count, props);
    bool found = false;
    for (uint32_t i = 0; i < count; i++)
        if (strcmp(props[i].layerName, name) == 0) { found = true; break; }
    free(props);
    return found;
}

static void create_instance(SDL_Window *win)
{
    uint32_t sdl_ext_count = 0;
    SDL_Vulkan_GetInstanceExtensions(win, &sdl_ext_count, NULL);

    const char **exts = malloc((sdl_ext_count + 1) * sizeof(char *));
    SDL_Vulkan_GetInstanceExtensions(win, &sdl_ext_count, exts);
    uint32_t ext_count = sdl_ext_count;

    const char *validation_layer = "VK_LAYER_KHRONOS_validation";
    bool use_validation = false;

#ifdef I76_VULKAN_VALIDATION
    if (layer_available(validation_layer)) {
        use_validation = true;
        exts[ext_count++] = VK_EXT_DEBUG_UTILS_EXTENSION_NAME;
        fprintf(stdout, "[render_vk] Validation layers enabled.\n");
    } else {
        fprintf(stdout, "[render_vk] Validation layers not found "
                "(install vulkan-validation-layers). Running without.\n");
    }
#endif

    VkApplicationInfo app = {
        .sType              = VK_STRUCTURE_TYPE_APPLICATION_INFO,
        .pApplicationName   = "Interstate '76 Nitro Pack",
        .applicationVersion = VK_MAKE_VERSION(0, 1, 0),
        .pEngineName        = "i76",
        .engineVersion      = VK_MAKE_VERSION(0, 1, 0),
        .apiVersion         = VK_API_VERSION_1_0,
    };

    VkInstanceCreateInfo ci = {
        .sType                   = VK_STRUCTURE_TYPE_INSTANCE_CREATE_INFO,
        .pApplicationInfo        = &app,
        .enabledExtensionCount   = ext_count,
        .ppEnabledExtensionNames = exts,
        .enabledLayerCount       = use_validation ? 1 : 0,
        .ppEnabledLayerNames     = use_validation ? &validation_layer : NULL,
    };

    VK_CHECK(vkCreateInstance(&ci, NULL, &s_instance));
    free(exts);
}

/* -----------------------------------------------------------------------
 * Physical device + queue families
 * ----------------------------------------------------------------------- */

static bool check_device(VkPhysicalDevice dev,
                         uint32_t *out_gfx, uint32_t *out_present)
{
    /* Queue families */
    uint32_t qf_count;
    vkGetPhysicalDeviceQueueFamilyProperties(dev, &qf_count, NULL);
    VkQueueFamilyProperties *qf = malloc(qf_count * sizeof(*qf));
    vkGetPhysicalDeviceQueueFamilyProperties(dev, &qf_count, qf);

    int gfx = -1, present = -1;
    for (uint32_t i = 0; i < qf_count; i++) {
        if (qf[i].queueFlags & VK_QUEUE_GRAPHICS_BIT) gfx = (int)i;
        VkBool32 ps = VK_FALSE;
        vkGetPhysicalDeviceSurfaceSupportKHR(dev, i, s_surface, &ps);
        if (ps) present = (int)i;
    }
    free(qf);
    if (gfx < 0 || present < 0) return false;

    /* VK_KHR_swapchain extension */
    uint32_t ec;
    vkEnumerateDeviceExtensionProperties(dev, NULL, &ec, NULL);
    VkExtensionProperties *ep = malloc(ec * sizeof(*ep));
    vkEnumerateDeviceExtensionProperties(dev, NULL, &ec, ep);
    bool has_sc = false;
    for (uint32_t i = 0; i < ec; i++)
        if (strcmp(ep[i].extensionName, VK_KHR_SWAPCHAIN_EXTENSION_NAME) == 0)
            { has_sc = true; break; }
    free(ep);
    if (!has_sc) return false;

    /* Swapchain has at least one format and one present mode */
    uint32_t fmt_count, pm_count;
    vkGetPhysicalDeviceSurfaceFormatsKHR(dev, s_surface, &fmt_count, NULL);
    vkGetPhysicalDeviceSurfacePresentModesKHR(dev, s_surface, &pm_count, NULL);
    if (!fmt_count || !pm_count) return false;

    *out_gfx     = (uint32_t)gfx;
    *out_present = (uint32_t)present;
    return true;
}

static void pick_device(void)
{
    uint32_t count;
    vkEnumeratePhysicalDevices(s_instance, &count, NULL);
    if (!count) vk_fatal("No Vulkan-capable GPU found");

    VkPhysicalDevice *devs = malloc(count * sizeof(*devs));
    vkEnumeratePhysicalDevices(s_instance, &count, devs);

    s_phys = VK_NULL_HANDLE;
    bool found_discrete = false;

    for (uint32_t i = 0; i < count; i++) {
        uint32_t g, p;
        if (!check_device(devs[i], &g, &p)) continue;

        VkPhysicalDeviceProperties props;
        vkGetPhysicalDeviceProperties(devs[i], &props);
        bool discrete = (props.deviceType == VK_PHYSICAL_DEVICE_TYPE_DISCRETE_GPU);

        if (s_phys == VK_NULL_HANDLE || (!found_discrete && discrete)) {
            s_phys           = devs[i];
            s_gfx_family     = g;
            s_present_family = p;
            found_discrete   = discrete;
        }
    }
    free(devs);

    if (s_phys == VK_NULL_HANDLE) vk_fatal("No suitable GPU found");

    VkPhysicalDeviceProperties props;
    vkGetPhysicalDeviceProperties(s_phys, &props);
    fprintf(stdout, "[render_vk] GPU: %s\n", props.deviceName);
}

