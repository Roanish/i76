/*
 * render_vk.c — Vulkan backend for render.h
 *
 * Replaces NITSHELL.DLL. Implements:
 *   render_init / render_shutdown
 *   render_begin_frame / render_end_frame
 *   render_set_palette / render_blit_indexed
 *
 * Architecture:
 *   - SDL2 owns the window and creates the VkSurfaceKHR via SDL_Vulkan_*
 *   - One graphics+present queue (same family preferred)
 *   - Double-buffered swapchain (mailbox if available, FIFO fallback)
 *   - One descriptor set: binding 0 = index texture, binding 1 = palette UBO
 *   - Fullscreen triangle drawn every frame; fragment shader expands palette
 *   - Staging buffer for index texture uploads (persistently mapped)
 *   - Palette UBO is host-coherent (direct memcpy, no barrier needed)
 */

